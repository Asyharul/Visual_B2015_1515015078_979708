    public void cekisi() {
    if (nama.getText().length()==0 ||
        telp.getText().length()==0 ||
        kota.getSelectedItem()==null ||
        buttonGroup1.getSelection()==null ||
        jurusan.getSelectedItem()==null){
simpan.setEnabled(false);
     }
     else {
        simpan.setEnabled(true);
         
}
}
